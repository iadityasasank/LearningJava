package com.aditya;

public class OneDArrayDemo {
	
	public static void main(String[] args)
	{
		int nums[] =new int[4];
		//Or we can assign as int num[]={9,11155,44};
		nums[0] = 9;
		nums[1] = 11;
		nums[2] = 55;
		nums[3] = 44;
		
		for(int i=0; i<4;i++)
		{
			System.out.println(nums[i]);
		}
		
		
	}

}
